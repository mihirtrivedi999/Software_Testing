package com.a143;

public class Relationalop
{
	
	public static void main(String[] args) {
		
		
		
		int a=10;
		int b=11;
		int c=10;
		
		
		System.out.println(a==b);
		System.out.println(a==c);
		
		
		System.out.println(a!=b);
		System.out.println(a!=c);
		
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<=c);
		System.out.println(a>=c);
		
		
		
	}
	

	
}